package cn.tjusic.st;

public class Calculate {

    public int add (int a, int b) {
        return a + b;
    }

    public int substract(int a, int b) {
        return a - b;
    }

    public int divide(int a, int b) {
        return a / b;
    }

    public int multiple(int a, int b) {
        return a * b;
    }
}
